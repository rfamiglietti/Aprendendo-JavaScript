// 1. Criar uma variável chamada `nome` e atribuir a ela o valor "Ana Clara"
let nome = "Ana Clara";

// 2. Usar o método `.replace()` para substituir o texto "Ana" por "Maria"
// O método `.replace()` encontra a primeira ocorrência do texto especificado e substitui pelo novo valor.
let nomeModificado = nome.replace("Ana", "Maria");

// 3. Exibir o nome modificado no console
console.log(`Ana Clara
Nome modificado:`, nomeModificado);
