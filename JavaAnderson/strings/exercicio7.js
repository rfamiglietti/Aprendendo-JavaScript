// 1. Criar uma variável chamada `texto` e atribuir a ela o valor "Eu amo programar em JavaScript"
let texto = "Eu amo programar em JavaScript";

// 2. Usar o método `.includes()` para verificar se a palavra "JavaScript" está presente na string
if (texto.includes("JavaScript")) {
  // 3. Se a palavra "JavaScript" estiver presente, exibir "Palavra encontrada"
  console.log("Palavra encontrada");
} else {
  // 4. Caso contrário, exibir "Palavra não encontrada"
  console.log("Palavra não encontrada");
}

