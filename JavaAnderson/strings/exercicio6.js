// 1. Criar uma variável chamada `senha` e atribuir a ela o valor "JavaScript123"
let senha = "JavaScript123";

// 2. Verificar se a senha tem pelo menos 8 caracteres usando o método `.length`
// O método `.length` retorna o número total de caracteres na string.
if (senha.length >= 8) {
  // 3. Se o comprimento da senha for 8 ou mais, exibir "Senha válida"
  console.log("Senha válida");
} else {
  // 4. Caso contrário, exibir "Senha inválida"
  console.log("Senha inválida");
}
