//COMANDO: Crie uma variável chamada frase com o valor "Aprender JavaScript é divertido!". Use o método .length para exibir o número de caracteres na frase.


// 1. Criar uma variável chamada `frase` e atribuir a ela a string "Aprender JavaScript é divertido!"
let frase = "Aprender JavaScript é divertido!";

// 2. Usar o método `.length` para obter o número de caracteres na string armazenada em `frase`
// O método `.length` retorna o total de caracteres, incluindo letras, números, espaços e pontuações.
let numeroDeCaracteres = frase.length;

// 3. Exibir o número de caracteres no console
console.log(`Aprender JavaScript é divertido!
Número de caracteres na frase:`, numeroDeCaracteres);