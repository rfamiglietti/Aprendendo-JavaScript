


// 1. Criar uma variável chamada `texto` e atribuir a ela a string "JavaScript é incrível"
let texto = "JavaScript é incrível";

// 2. Usar o método `.toUpperCase()` para converter todos os caracteres da string para letras maiúsculas
// O método `.toUpperCase()` retorna uma nova string com todos os caracteres transformados para maiúsculas
let textoMaiusculo = texto.toUpperCase();

// 3. Exibir a string em letras maiúsculas no console
console.log(`JavaScript é incrível 
Texto em maiúsculas:`, textoMaiusculo);
